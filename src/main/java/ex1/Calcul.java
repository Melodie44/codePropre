package ex1;

/**
 * Classe regroupant les méthodes de calcul entre plusieurs nombres.
 * 
 * @author Maubois Mélodie
 *
 */
public class Calcul {

	/**
	 * Méthode pour additionner deux nombres.
	 * 
	 * @param a, nombre entier.
	 * @param b, nombre entier.
	 * @return le résultat de l'addition sous forme d'un nombre entier.
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode pour soustraire deux nombres.
	 * 
	 * @param a, nombre entier.
	 * @param b, nombre entier.
	 * @return le résultat de la soustraction sous forme d'un nombre entier.
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
